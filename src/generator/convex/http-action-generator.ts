  /**
   * Generate POST (create) endpoint
   */
  private generatePost(
    table: TableInfo,
    tableName: string,
    pascalName: string
  ): string {
    return `/**
 * POST ${this.options.basePath}/${tableName}
 * Create a new ${pascalName}
 */
export const create = httpAction(async (ctx, request) => {
  // Handle CORS preflight
  if (request.method === "OPTIONS") {
    return new Response(null, { status: 204, headers: corsHeaders });
  }

  ${
    this.options.includeAuth
      ? `// Verify authentication
  const auth = await verifyAuth(request);
  if (!auth.authenticated) {
    return errorResponse("Unauthorized", 401);
  }`
      : ''
  }

  try {
    // Parse request body
    const body = await request.json();

    if (!body || typeof body !== "object") {
      return errorResponse("Invalid request body", 400);
    }

    // Create document
    const id = await ctx.runMutation(api.${tableName}.create, body);

    return jsonResponse(
      {
        data: { id },
        message: "${pascalName} created successfully",
      },
      201
    );
  } catch (error) {
    console.error("Error creating ${tableName}:", error);

    // Handle validation errors
    if (error instanceof Error && error.message.includes("validation")) {
      return errorResponse(error.message, 400);
    }

    return errorResponse(
      error instanceof Error ? error.message : "Internal server error",
      500
    );
  }
});`;
  }

