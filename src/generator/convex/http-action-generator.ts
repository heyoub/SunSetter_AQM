  /**
   * Generate GET list endpoint
   */
  private generateGetList(
    table: TableInfo,
    tableName: string,
    pascalName: string
  ): string {
    return `/**
 * GET ${this.options.basePath}/${tableName}
 * List all ${pascalName} documents with optional pagination
 */
export const list = httpAction(async (ctx, request) => {
  // Handle CORS preflight
  if (request.method === "OPTIONS") {
    return new Response(null, { status: 204, headers: corsHeaders });
  }

  ${
    this.options.includeAuth
      ? `// Verify authentication
  const auth = await verifyAuth(request);
  if (!auth.authenticated) {
    return errorResponse("Unauthorized", 401);
  }`
      : ''
  }

  try {
    // Parse query parameters
    const url = new URL(request.url);
    const numItems = parseInt(url.searchParams.get("limit") ?? "50");
    const cursor = url.searchParams.get("cursor") ?? null;

    // Fetch documents using Convex pagination
    const result = await ctx.runQuery(api.${tableName}.list, {
      paginationOpts: { numItems, cursor },
    });

    return jsonResponse({
      data: result.page,
      pagination: {
        hasMore: !result.isDone,
        nextCursor: result.continueCursor,
      },
    });
  } catch (error) {
    console.error("Error listing ${tableName}:", error);
    return errorResponse(
      error instanceof Error ? error.message : "Internal server error",
      500
    );
  }
});`;
  }

